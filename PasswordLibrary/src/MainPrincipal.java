    private void addInfoComboBox(int number){
        switch (number){
            case 1://llenando los textField de modificar informacion de persona
                textField_firstName_victimModify.setText(person.getFirsName());
                textField_middleName_victimModify.setText(person.getMiddleName());
                textField_lastName1_victimModify.setText(person.getLastname1());
                textField_lastName2_victimModify.setText(person.getLastName2());
                break;
            case 2://lenando los jComboBox de modificar informacion de familias
                try {
                    jComboBox_modify_families.removeAllItems();
                    person.getFamilies().stream().forEach((fam) -> {
                        jComboBox_modify_families.addItem(fam.getDescription());
                    });
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error en la caja de familias.");
                }
                break;
            case 3://llenando la jComboBox de modificar informacion de mascotas
                try {
                    jComboBox_modify_pets.removeAllItems();
                    person.getPets().stream().forEach((pet) -> {
                        jComboBox_modify_pets.addItem(pet.getDescription());
                    });
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error en la caja de mascotas.");
                }
                break;
            case 4://llenando la jComboBox de modificar informacion de fechas
                try {
                    jComboBox_modify_dates.removeAllItems();
                    person.getDates().stream().forEach((date) -> {
                        jComboBox_modify_dates.addItem(date.getDescription());
                    });
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error en la caja de mascotas");
                }
                break;
            case 5://Llenando la jComboBox de modificar la informacion extra
                try {
                    jComboBox_modify_extra.removeAllItems();
                    person.getExtra().stream().forEach((extra) -> {
                        jComboBox_modify_extra.addItem(extra.getDescripction());
                    });
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(this, "Error en la caja de informacion extra.");
                }
                break;
        }//fin del switch
    }
